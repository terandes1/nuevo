                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 3. - Lima Cusco and the Sacred Valley. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Flight from Lima to Cusco - Transfer to Urubamba, en route visit to Awanakancha.
                                                Morning departure to the airport for your flight to Cusco, on your arrival our representative will meet you and transfer you to the sacred valley.
                                                Accompanied by our expert local photographer, we will get some tips and tricks to capture the best pictures of every site we will see along the way. 
                                                Our first stop will be the center of South American Camelids of Awanakancha and its Living Museum, here you can marvel yourself seeing weavers at work. They will be sharing the beauty of their textile tradition with you; there will be also a great opportunity to see and feed South American camelids such as llamas, alpacas, vicuñas and other species.  
                                                After seeing this nice spot we will continue our way to a nice hacienda where you will have lunch.
                                                In the afternoon, we will have our first adventure in the valley; we will enjoy the surroundings of the area on horses.
                                                First, we will have a safety briefing before we take a short horseback ride through farming areas on beautiful Peruvian Paso horses.

                                                Overnight in the Sacred Valley.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 4. - Pisac ruins & Market, visit to Inkariy Museum. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Morning tour of Pisac Ruins and its handicraft market.
                                                Today, accompanied by our guide and our expert photographer we are going to experience Peru’s fascinating combination of past and present. First, we will visit the market, located in the main square of Pisac to witness its vibrant activity. The market shows its colorful textiles, all kinds of dyes, cutlery, ceramics and its vast variety of fresh vegetables; it seems that this place holds something to please everyone. Then we will, head to the impressive Inca ruins of Pisac, located on top of the mountain that overlooks the town, to explore its incredible constructions. We will have some tips and tricks of our photographer to get the best pictures of the site and its surroundings.
                                                On the way back from the ruins, we will stop in a nice setting to enjoy a picnic lunch.
                                                After lunch, we will visit the Inkariy museum, an interesting place located not far away from the town of Calca. Here we will have some insights about the Peruvian pre Inca civilizations up to the Incas and we will deepen our understanding of the Peruvian culture.
                                                Overnight in the Sacred Valley.

                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 5. - Maras & Moray – activities.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Full day tour to visit the agricultural terraces of Moray and the Saltpans of Maras.
                                                This morning, we will have a great opportunity to deepen our photographic skills and get exceptional pictures of the places we are going to visit; we will depart from our hotel towards the upper part of Urubamba city heading to the enigmatic Inca ruins of Moray. From the upper part, these circular terraces look like an ancient amphitheater, though some experts believe they served as agricultural laboratories where the Incas could experiment with new farming techniques as well as a process of acclimatization of some plants. Of course, this is just one of the various theories, and even after many years of research and dozens of hypotheses, but still the true purpose of Moray remains as a mystery. This day, you are going to inspect the site yourself and have your own theories.
                                                Depart from the intriguing Moray and continue to the small town of Maras, home of an old salt mine that dates from the Inca times. This place comprises thousands of milky pools, each set on its own terrace along the hillside, the saltpans are particularly surreal in appearance and incredibly photogenic. Members of the community own the pans and they have been passed down through the local families since pre-Inca times.
                                                After seeing this site, we will drive to Maras, where we will have a picnic lunch.
                                                After lunch, we will visit the town of Maras where we will take part on some activities such as knitting in corn leaves, working on ceramics or visiting the hat maker. 
                                                At the end of the day, we will drive you back to your hotel.
                                                Overnight in the Sacred Valley.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 6. - Ollantaytambo & Chinchero – paddling or kayaking in Huaypo lagoon.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Visit to the Inca site of Ollantaytambo, the town of Chinchero and Huaypo lagoon.
                                                This morning we will have an early departure to visit the town and the impressive fortress of Ollantaytambo, this place is one of the living examples of ancient city planning. It is a charming town characterized by its cobblestone streets and beautiful stone houses with big interior courtyards and the unusual trapezoidal doors that retain the traditional Inca design. The streets in this town have water canals running down towards the farming lands and the river.
                                                The fortress is located on top of the mountain that overlooks the town; it has a religious area with an impressive stonework. In the lower part, there is a section of wide terraces.
                                                After seeing this beautiful place, we will drive towards the lake of Huaypo, located in the nearby area of Chinchero where we will have a time in the lake for paddling or kayaking. 
                                                Once we arrive at the lagoon, our staff will be waiting for us in a tent with everything necessary for our activity. We will have something to drink and maybe some snacks to eat. We will relax for a moment and enjoy the beautiful scenario. After our drinks, with our guide, we will have our safety briefing, then our staff will provide us all the necessary equipment for the activity, such as life jacket, wind jacket, skirt for kayak and a rowing.
                                                After receiving the instructions, we will be ready to start our adventure in the lake. It will take approximately one hour and a half, here we will appreciate some birds and maybe some fish of this crystalline lagoon, we will have the chance to take photos of the incredible scenario and the surrounding mountains, after the tour, we will return to our base where we will enjoy a picnic lunch.
                                                After lunch, we will head towards Chinchero to visit the old part of the town to explore some Inca constructions and its beautiful colonial church. The town is famous for the extraordinary beauty and quality of its traditional textiles, and for its Sunday fair, where local people gather wearing their traditional colorful outfits traveling down from their villages to trade their goods. Your guide will take you to the workshop of local weavers to learn more about the efforts of the community to keep alive their textile tradition and iconography, passing them to the young generations, especially women.
                                                Overnight in the Sacred Valley
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 7. - Short hike to Machu Picchu.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Short Inca trail to the citadel of Machu Picchu
                                                This morning, we will transfer you to Ollantaytambo station to board the Vistadome train to the starting point of the short Inca Trail, Km 104 of the route to Machu Picchu.
                                                With our expert photographer, we will have a chance to get wonderful pictures of the area.  
                                                On our arrival, we will get ready to start the hike; we will go through the checkpoint and cross a bridge over the Urubamba River. We are going to start visiting the archaeological complex of Chachabamba, located not far away from the control point, then we will embark on an 8 km. ascending trail, which after about four hours will take us to the archaeological complex of Wiñaywayna. This is an impressive Inca complex made up of an agricultural center with numerous terraces, a religious sector and an urban sector. On the way up, we will have magnificent views of the Urubamba River valley and the Choquesuysuy archaeological complex located on the other side of the river; we will also see a beautiful waterfall located just a few minutes away from Wiñaywayna. We will continue our hike and climb to Intipunku or The Sun Gate. This will take an hour of hiking along a trail of flat stones on the edges of cliffs in the raincloud forest. Once we reach this point, we will have a fantastic view of the lost citadel of Machu Picchu. From Intipunku we will descend to Machu Picchu and then by bus to Aguas Calientes. 
                                                For the hike, we will have a box lunch and an assistant (Porter) to help us with the food and water supply. Dinner at the hotel.
                                                Overnight in Aguas Calientes.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 8. – Machu Picchu - Cusco.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Second guided visit of Machu Picchu
                                                This morning, accompanied by your guide, you will visit the incredible lost citadel of Machu Picchu. You will explore this fascinating Inca site from side to side, visiting its different sectors, enjoying astonishing views of the site and the surrounding scenery.
                                                Our expert photographer will help us to get the best pictures of the site and he will make some special pictures of us in different locations of this spectacular citadel. 
                                                Buffet lunch at Sanctuary Lodge Hotel included.
                                                In the afternoon, we will head to the train station to board our train back to Poroy station. On your arrival, our representative will be waiting for you at the train station to transfer you to the hotel in Cusco.
                                                Overnight in Cusco.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 9. - Cusco nearby ruins.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Morning tour to visit the incredible >Inca sites built overlooking the city. 
                                                Morning tour to visit the nearby ruins of the city, located overlooking the valley of Cusco. We will drive towards the north, to the upper part where we are going to explore Sacsayhuaman, an incredible megalithic construction that in the past was considered as a fortress, but recent excavations proved that it was a religious center. We will continue our explorations visiting Q’enko, a ceremonial place where we will see its labyrinth and a very particular structure similar to an amphitheater. Our last spot will be Puca Pucara, a small structure that used to be a checkpoint for visitors entering the city of Cusco.
                                                On the way back to the hotel, we will stop at San Cristobal, here we will have great views of the city and we will enjoy some drinks.
                                                We will have a visit to the studio of one of the most representative photographers of Cusco in the early 20th century; we will have a chance to appreciate his huge collection of pictures of the city and its surroundings. Return to the hotel.
                                                Overnight in Cusco.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 10. - Southern Valley.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Full day tour to visit Tipon, Pikillacta and Andahuaylillas.
                                                This morning, after breakfast we will pick you up to start out tour towards the Southern valley, first, we will visit Tipon; an important archaeological site that holds numerous impressive terraces that are still being cultivated today. The most interesting part of the site is the irrigation system that still serves agricultural lands in the nearby areas. It consists of carved stone channels, precisely calculated and sometimes with almost vertical falls that all together constitute a hydraulic engineering masterpiece.
                                                We will continue our visit heading to Pikillacta. The constructions of this pre-Inca town are composed of more than 700 structures including 200 departments (Kanchas), 504 storages (qolqas) and different buildings that probably gave shelter to a population of around ten thousand inhabitants. The city was constructed in a harmonious and symmetric way, in blocks with straight streets that embraced many sectors such as the administrative, ceremonial, urban, defensive and a road system. 
                                                The inhabitants constructed the walls with a combination of mud and stone, all of them wide at the bottom and narrow at the upper part. Room walls and floors were also made with a kind of thick plaster showing the appearance of a white city.
                                                Lunch in a Local restaurant.
                                                Our last spot to visit is Andahuaylillas, considered as one of the most important attractions, here the church of San Pedro de Andahuaylillas is known and called the “Sistine Chapel of the Americas” due to the impressive quality of its art pieces and frescoes that decorate the walls. Built at the beginning of XVII century, the interior of the temple is decorated with murals of Luis Riaño (XVII, the paintings represent the path to glory and the path to hell. Besides, this church holds incredible treasures, like an impressive old organ, numerous canvases of the Academy of Cusco, silver work and baroque altars.     Afternoon return to your hotel.
                                                Overnight in Cusco.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>DAY 11. - Cusco City highlights.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Morning tour of the city
                                                This morning accompanied with your guide, you will explore the colonial streets and the squares of Cusco. You will immerse in the fascinating history of the Inca Capital. The first spot to visit will be the Santo Domingo church, a colonial temple that lays on top of the ancient Inca Temple of the Sun (Koricancha); then you will continue the tour heading towards the Cathedral of the city, located right in the main square – this is one of the most impressive exhibits of colonial art.
                                                Our walk will continue through the beautiful streets of the traditional neighborhood of San Blas, which is well known for being the house of the most representative Masters of Arts in Cusco city, San Blas is considered the cradle of traditional Cusquenian art.  Then you are going to descent towards the hotel, en route you will pass by the famous 12-angled stone, whose image has been taken in many TV commercials.
                                                After the tour, we will take you to a restaurant where you will have lunch.
                                                The rest of the day will be at leisure, you can opt to continue exploring the city on your own or maybe do some shopping around the central part of the city or if you prefer, you can relax and enjoy the facilities in your hotel.
                                                Overnight in Cusco.
                                                </p>
