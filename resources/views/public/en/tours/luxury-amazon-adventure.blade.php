                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 4. – Amazon Cruise.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Moringo Lake – Nauta Caño.
                                                This morning you will depart in skiffs to explore and fish for the renowned piranha at Moringo Lake where you will experience the transition from the muddy waters of the Amazon River to the black waters of tributaries, here you may see local fishermen in their dugout canoes. Look out for wildlife such as kingfishers, hawks and monkeys, just a snapshot of what you may be lucky to see.
                                                In the afternoon, you will embark in an excursion to Nauta Caño, part of the Pacaya Samiria Reserve renowned for its mirrored lagoons, which reflect the jungle scenery. Here you may observe toucans, macaws, and iguanas, among many other species. As you make your way back to the boat for an evening Pisco sour, take in the views of an incredible Amazon sunset.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 5. – Amazon Cruise.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Yanayacu – Pucate River.
                                                This morning, before breakfast, you will visit the Yanayacu – Pucate River in Pacaya Samiria National Reserve. Again, we will be travelling through black water rivers, look out for brightly colored macaws and several species of primates including capuchins and squirrel monkeys. You may even catch a glimpse of the famous Pink or Grey River Dolphins. Stop for a tasty breakfast of freshly baked goods, fresh juice and a cup of tea or coffee.
                                                After lunch, you will have some time to relax and rest before taking part on a lecture given by the expert naturalists on board. The talk will be about the marine life of the Amazon and how the cruise company works providing equipment and advice to local fishermen. 
                                                Late in the afternoon, sail along the Maranon River towards the Casual community, along the way looking out for the array of species of flora and fauna. Here we will have an opportunity to walk on land for an alternative view of the Amazon, perhaps spot tanagers and orioles as well as various reptiles and amphibians. Keep an eye out for the beautiful and eye-catching but poisonous frogs along this fascinating trail, as well as trees and medicinal plants.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 6. – Amazon Cruise. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Explore riverside communities.
                                                After breakfast, we will board the skiff to move around the edges of Pacaya-Samiria Reserve, a flooded forest covering five million acres at the headwaters of the Amazon basin. Witness the incredible sunrise over the birthplace of the Amazon River. Here two major tributaries, the Ucayali and the Marañon, converge to ‘give birth’ to the world’s greatest waterway. Look for the morning bird life, the area is home five species of parakeets, parrots, laughing falcons and gray tanagers among many others.
                                                Later this morning, we are going to visit one of the Amazonian riverside communities. Here we will learn about their way of life and we may buy artisanal handicrafts to take home. En route back to the boat we will see the world’s largest aquatic plant, the giant Victoria Regia water lilies.
                                                Some passengers will depart today after the three-night trip. Those staying on board have the opportunity to explore Nauta as part of a land excursion to see the oldest town in Loreto department, an important frontier town for the local communities where they sell their products. Others may venture to Nauta Caño within the Pacaya-Samiria Reserve. Return to the boat at sunset where you meet new passengers joining for the four-night voyage.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 7. - Amazon Cruise. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Yanallpa River – Dorado River.
                                                After breakfast within the vast Amazon flooded forest of the Yanallpa River, we will cruise along an amazing black water stream, as your guides tell you about the rainforest. We will explore what scientists call a “gallery forest,” an area characterized by its narrow vegetation that creates a tunnel of leaves. As we float through this tunnel, your naturalist guides will help you to spot parrots, macaws, and fluffy monk saki monkeys in the trees along the riverbanks.
                                                After lunch, we will take part of an interesting talk on the Taricaya Turtles, their near extinction and the recent efforts supported by Aqua Expeditions that are successfully growing the turtle’s population in the Pacaya-Samiria Nature Reserve.
                                                Late in the afternoon, we will travel by skiff through the “Mirrored Forest” to see towering trees reflected in the black water Dorado River looking out for parrots, blue-yellow macaws, and the great variety of primates and sloths unperturbed by our presence. By night, spot caiman and frogs as well as fishing bats before heading back to the boat.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 8. - Amazon Cruise. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Pacaya River.
                                                This morning, we will take a full day excursion along the Pacaya River, observing and exploring the flooded forest of this vast region of the Peruvian Amazon. Visit some of the most beautiful lakes within the protected reserve and meet some of the friendly locals who take care of the reserve’s natural resources.
                                                Your guides will explain Aqua Expeditions’ involvement in the natural resource management of Amazonia. We will have a picnic lunch en route and, on our return, we are going to see the famous Ceiba tree, the highest in the Amazon. During this excursion, keep a keen eye out for inquisitive Pink and Grey River Dolphins as well as monkeys, birds, arboreal lizards, iguanas.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 9. - Amazon Cruise. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Lake Clavero – Amazon River.
                                                This day, we will sail to Lake Clavero where you will sight villagers paddle in their traditional dugout canoes. Perhaps take a refreshing swim in the lake’s calm waters and visit the community to interact with the friendly Amazon villagers. Return to the Aqua Amazon to sail back down river towards Iquitos.
                                                After lunch, we will sail along the great Amazon River, observing the flora and fauna, then stop at a riverside community who actively conserve the forest’s natural resources and their own ancestral customs.  Return to the Aqua Amazon in the evening as the ship continues downstream, sailing toward Iquitos through the night.
                                                Following dinner, your naturalist guides introduce the important crewmembers whom you may not have met, as they have been busy “behind the scenes” to create your seamless Aqua Expeditions experience.
                                                Overnight at Aria Amazon
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 10. - Amazon – Iquitos – Lima</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Tour to visit some communities and flight back to Lima.
                                                Breakfast at 7:30 followed by a morning excursion dependent on the level of the river. Weather and time permitting, we will depart for a final chance to see how communities survive along the Amazon, visiting the lively village of Belen, here, the houses and shops float on the river itself during some months of the high-water season.
                                                After lunch, we will disembark our cruise to visit the Manatee Rescue Centre, where biologists and volunteers care for endangered Amazon manatees rescued by the local conservation authorities. Learn about this highly successful project, which has been supported by Aqua Expeditions since its inception, and all efforts to help these endearing mammals. See how the manatees are cared for and prepared for re-introduction into their natural habitat. Check-in at Iquitos International Airport on time for your flight to Lima.
                                                On your arrival to Lima airport, our representative will meet you to transfer you back to the hotel in Barranco.
                                                Overnight at B Arts Boutique Hotel
                                                </p>
