                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 3. - Lima – Iquitos </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Flight from Lima to Iquitos and transfer to your cruise.
                                                This morning, at a scheduled time we will transfer you to the airport for the flight to Iquitos. Upon your arrival in Iquitos airport, you will meet your cruise representative and guides who will assist you for the scenic transfer to Nauta; the ride will take about one hour and a half; on your arrival to Nauta, you will board your cruise.
                                                Check in and settle into the comforts of your river-facing suite before the boat gets ready to depart from Iquitos for your upcoming adventures in the Amazon. Over the next days you will sail along the largest tributaries of the Amazon River and the mighty Amazon itself, towards remote areas reached by very few. You will meet the cruise director who will introduce you to the expert naturalist guides; they will give details about the upcoming activities.
                                                Every day you will have a variety of exciting excursions to experience the best of the Amazon such as exploring the remote tributaries by skiff, fishing for piranhas, looking out for birds, mammals and reptiles and back on board there will be interesting lectures given by the expert naturalist guides. These may cover aspects of the region such as the environment, communities, flora and fauna.
                                                Please note that itineraries are subject to change according to prevailing conditions such as climate and season.
                                                Overnight at Aria Amazon Cruise.
                                                </p>
