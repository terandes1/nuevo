                                            <div class="acc-title"><span class="acc-icon"></span>Day 5. - The Sacred Valley and its treasures.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Visit to Inkariy museum and Horseback riding tour.
                                                This morning, after breakfast we will drive to visit the Inkariy Museum, a place that compiles the most representative periods of development of the Peruvian civilization. Here accompanied by your guide you will get insights of the importance of the pre-Inca civilizations up to the Incas. After this visit, we will drive to a local hacienda where we will learn how the preparation of a typical dish is made; accompanied by the chef, first you will learn about the ingredients and how they are combined and put together to create a delicious and tasty dish.
                                                After lunch, you will take part of a familiarization and safety briefing to start your next adventure, a short horseback ride in the surroundings of the hacienda, along our ride we will learn about some farming techniques and the types of products that are grown in these farming fields. 
                                                Return to your hotel.
                                                Overnight in the Sacred Valley.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 6. – Chinchero and Moray.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Visit to the town of weavers and the agricultural terraces. 
                                                This morning, we will drive to visit the town of Chinchero, it is well known because of its textile tradition, here you will have an opportunity to talk and interact with the weavers who will share some of their knowledge with you.
                                                After visiting this place, our tour will continue with the visit of the enigmatic Inca ruins of Moray. From the upper part, these circular terraces look like an ancient amphitheater, though some experts believe they served as agricultural laboratories where the Incas could experiment with new farming techniques as well as a process of acclimatization of some plants. Of course, this is just one of the various theories, and even after many years of research and dozens of hypotheses, but still the true purpose of Moray remains as a mystery. This day, you are going to inspect the site yourself and have your own theories.
                                                Overnight in the Sacred Valley.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 7. - Ollantaytambo – Machu Picchu.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Visit to Ollantaytambo Inca Fortress and Machu Picchu citadel.
                                                This morning we will depart early from the hotel to Ollantaytambo, we will explore the Inca site. 
                                                 Ollantaytambo is a charming Inca town characterized by its cobblestone streets and beautiful stone houses with big interior courtyards and the unusual trapezoidal doors that retain the traditional Inca urban planning. The streets in this town have water canals running down towards the farming lands and the river.
                                                The fortress is located on top of the mountain that overlooks the town; it has a religious area with an impressive stonework. In the lower part, there is a section of wide terraces.
                                                At the end of this visit, we will transfer you to the train station where you will board the train for our journey to Aguas Calientes.
                                                Enjoy this scenic ride, where you will see fantastic landscapes and how the vegetation changes, from the almost brown, grassy highland scenery to the incredible green of the cloud forest of Machu Picchu area. Upon your arrival to Aguas Calientes, the hotel staff will meet you and help us taking the bags to the hotel. Accompanied by our guide we will walk to your hotel where you will have a chance to freshen up and get ready for the afternoon tour to the fabled lost citadel of Machu Picchu.
                                                We will have lunch at the hotel’s restaurant before we start our ascent to the lost citadel. After a 25 minutes bus ride, we will arrive to the entrance of this wonder of the world, accompanied with your private guide you will explore this mysterious and intriguing citadel – Machu Picchu. The incredible Inca stone construction will show up in front of you, sometimes depending on the weather conditions, the site will be alternately hidden and revealed by clouds; this natural phenomenon provides a mysterious and magical atmosphere to the site. After the tour, return to Aguas Calientes. 
                                                Overnight in Aguas Calientes. 
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 8. - Machu Picchu – Cusco.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Second guided visit to Machu Picchu and optional activities.
                                                The second day of Machu Picchu will offer you some options to do, you may decide to stay in your hotel and take some tours of nature and birds in the gardens, and enjoy the comfort of the hotel until the departure time.
                                                The second option is to go back to the Inca site accompanied with your guide and take a light walk through the citadel enjoying the views and taking a time to capture the best pictures of the place.
                                                The third option is to hike the iconic peak of Huayna Picchu, located in the background of the classic Machu Picchu vista. Once you reach the summit, you will have the chance to enjoy an alternate perspective of Machu Picchu. 
                                                Please keep in mind that in order to do this option, we need to secure permits in advance due to the limited number of entrances per day. 
                                                After doing any of these options, you will return to your hotel for lunch, here you will learn how to prepare some cocktails and desserts using local ingredients. 
                                                In the afternoon, you will be assisted to go to the train station to board the train back to Cusco. 
                                                On your arrival to the station, our representative will meet you for the transfer to your hotel.
                                                Overnight in Cusco.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 9. -   Cusco's culinary history and cooking classes.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Cusco city tour and cooking classes.
                                                This morning, you will learn about the history of the cuisine of Cusco. We will walk to the local market to get the ingredients for the dish to prepare in the cooking class. You will learn to cook traditional dishes such as red quinoa and yellow chili pepper that are a special part of Peru’s culture and heritage.
                                                In the afternoon we will visit the historic part of the city of Cusco, accompanied by our expert guide, we will walk through the traditional streets of Cusco. As we get to explore our first site, our guide will provide us information about the history of the city. Our visit will take us to Santo Domingo church; a colonial religious building that lays on top of the ancient Inca temple of the sun. We will continue our tour visiting the Cathedral of Cusco, a place that is a must see in order to get a better understanding of the architecture and art changes that took place in the colonial period.
                                                Then we will return to the hotel and the rest of the afternoon will be at leisure, you can continue exploring the city and maybe go shopping or if you prefer you can relax and enjoy the comforts in your hotel.
                                                Overnight in Cusco.
                                                </p>
                                            </div>
                                        </div>
                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 10. - Visit the Southern Valley. </div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Full day tour to Tipon- Pikillacta-Andahuaylillas.
                                                This morning, after breakfast we will pick you up to start out tour towards the Southern valley, first, we will visit Tipon; an important archaeological site that holds numerous impressive terraces that are still being cultivated today. The most interesting part of the site is the irrigation system that still serves agricultural lands in the nearby areas. It consists of carved stone channels, precisely calculated and sometimes with almost vertical falls that all together constitute a hydraulic engineering masterpiece.
                                                We will continue our visit heading to Pikillacta. The constructions of this pre-Inca town are composed of more than 700 structures including 200 departments (Kanchas), 504 storages (qolqas) and different buildings that probably gave shelter to a population of around ten thousand inhabitants. The city was constructed in a harmonious and symmetric way, in blocks with straight streets that embraced many sectors such as the administrative, ceremonial, urban, defensive and a road system. 
                                                The inhabitants constructed the walls with a combination of mud and stone, all of them wide at the bottom and narrow at the upper part. Room walls and floors were also made with a kind of thick plaster showing the appearance of a white city.
                                                Lunch in a Local restaurant. Here we will take part in a cooking demonstration class and the preparation of some drinks.
                                                Our last spot to visit is Andahuaylillas, considered as one of the most important attractions, here the church of San Pedro de Andahuaylillas is known and called the “Sistine Chapel of the Americas” due to the impressive quality of its art pieces and frescoes that decorate the walls. Built at the beginning of XVII century, the interior of the temple is decorated with murals of Luis Riaño (XVII, the paintings represent the path to glory and the path to hell. Besides, this church holds incredible treasures, like an impressive old organ, numerous canvases of the Academy of Cusco, silver work and baroque altars.
                                                Afternoon return to your hotel.
                                                Overnight in Cusco.
 
                                                </p>
                                            </div>
                                        </div>
