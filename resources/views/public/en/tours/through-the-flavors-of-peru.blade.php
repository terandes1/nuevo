                                            <div class="acc-title"><span class="acc-icon"></span>Day 2. - Exploring the local food markets and learn to make Pisco Sours and ceviche.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Full day to explore Lima, Cooking classes before lunch and City tour.
                                                This morning we will start our culinary adventure with a visit to a typical local food market. Accompanied by your guide you will see a wide variety of products that are unique to Peru, here you will learn about some cooking secrets and how the local ingredients are used to create those special flavors you find in our cuisine. After visiting the market, we will drive to the “Señorio de Sulco” restaurant where we will learn to make Pisco Sours and ceviche, which you will have for lunch. At the restaurant, you will find out that the owner has written several cookbooks and her love for traditional Peruvian food is evident throughout the restaurant.
                                                In the afternoon, we will embark on a tour of the highlights of the city of kings; we will see the two sides to Lima – the colonial and the modern. Here you are going to see the Plaza Mayor; the square shows beautiful examples of colonial and republican architecture in the surrounding houses. You will see the Government’s palace from the outside and you will visit the Cathedral of Lima. You will continue your tour heading to the Convent of San Francisco; this religious building is interesting because subterranean catacombs are one of the main attractions. We are going to finish the tour visiting the Casa Aliaga, which is the oldest western hemisphere’s mansion; this historic house is still inhabited and under the property of the 17th generation of the Aliaga family.  
                                                We will continue our way seeing the districts of Miraflores and San Isidro.
                                                Overnight in Lima.
                                                </p>
