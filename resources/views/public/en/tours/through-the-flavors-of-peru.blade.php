                                            <div class="acc-title"><span class="acc-icon"></span>Day 11.- Nearby Ruins of Cusco city.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Morning tour to visit the ruins in the surroundings of the city.
                                                This morning, we will head to the upper part of the city where we will visit the most representative Inca sites that surround the city, first, we will explore and admire the impressive Sacsayhuaman, a religious place located on a hill that overlooks the city; This Inca site was thought to be a fortress when the Spaniards arrived. Our journey will continue to visit Q’enko, an Inca shrine dedicated to worship the sun and the mother earth.
                                                On the way back to the city, we will stop at San Cristobal, here we will visit the remains of the Palace of the Inca Manco Capac, we will enjoy great views of the city and we will have some drinks. Return to the hotel.
                                                Overnight in Cusco.
                                                </p>
