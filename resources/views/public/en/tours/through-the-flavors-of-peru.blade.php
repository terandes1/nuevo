                                        <div class="acc-panel">
                                            <div class="acc-title"><span class="acc-icon"></span>Day 4. - Pisac and Sarapampa Hacienda.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Tour to Pisac ruins & market and a visit to the Hacienda of Sarapampa.
                                                This morning you will continue exploring the Sacred Valley; we will drive to Pisac to visit its ruins, located on top of the mountain that overlooks the town, we will walk through its incredible constructions and terraces. Then we will spend some time strolling around the local market that shows its colorful textiles, all kinds of dyes, cutlery, ceramics and its vast variety of fresh vegetables; it seems that this place holds something to please everyone.
                                                 After seeing this fantastic place, we will drive to the Sarapampa Hacienda, here we will have lunch, accompanied by the cooking staff, we will learn some secrets of the local cuisine.
                                                In the afternoon, we will visit the area where they farm the giant white corn, we will go through the cultivated areas to learn and understand the value of the corn in the local economy.  
                                                Return to the hotel. 
                                                Overnight in the Sacred Valley.
                                                </p>
                                            </div>
