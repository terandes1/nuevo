                                            <div class="acc-title"><span class="acc-icon"></span>Day 3. - Through the Andes to the Sacred Valley.</div>
                                            <div class="acc-body">
                                                <p style="text-align: justify; color: black">
                                                Lima – Cusco – Scared Valley.
                                                This morning, after breakfast in your hotel, we will take you to the airport for your morning flight to Cusco. As you start your flight, do not miss the stunning views of the landscapes, moving from the arid desert along the coast, towards the snow-capped mountains of the Andes.
                                                On your arrival, our representative will be waiting at the airport of Cusco to take you to the Sacred Valley, where you can easily acclimatize and adjust to the elevation change. 
                                                The Sacred valley is located just an hour away from Cusco, it is simply another world: cattle, showing cows and sheep grazing by the roadside, old farming terraces on the hillside that nowadays are still in use and impossibly steep peaks rising in the distance. En route, we will stop to visit the center of South American Camelids of Awanakancha and its Living Museum, here you can marvel and admire weavers at work, sharing the beauty of their textile tradition and there will be a unique opportunity to see and feed llamas, alpacas, vicuñas and other Andean camelids.
                                                We will continue our way to our hotel where we will have lunch.
                                                The rest of the afternoon will be at leisure.
                                                Overnight in the Sacred Valley. 
                                                </p>
